create database  cars charset=utf8;
/* Drop Tables */

DROP TABLE CAR;




/* Create Tables */

CREATE TABLE CAR
(
	CARID INT NOT NULL AUTO_INCREMENT,
	PINPAIMING VARCHAR(20),
	XILIE VARCHAR(20),
	GONGLISHU INT,
	SHOUJIA INT,
	YANSE VARCHAR(20),
	PAILIANG VARCHAR(20),
	ZHAOPIAN VARCHAR(200),
	PRIMARY KEY (CARID)
) charset=utf8;





